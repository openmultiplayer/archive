                        <h3>2. Cookie</h3>
                        <h4>2.1 Informazioni generali sui cookie</h4>
                        <p>
                            I cookie sono piccoli file di testo che i siti internet visitati dagli utenti inviano ai loro terminali (solitamente al browser), dove vengono memorizzati per essere poi
                            ritrasmessi agli stessi siti ad ogni successiva visita tramite il medesimo terminale. Inoltre, ogni sito può consentire la trasmissione dei c.d. cookie di "terze parti", cioé
                            quelli generati da siti internet diversi da quello che l'utente sta visitando (attraverso oggetti in esso presenti quali banner, immagini, mappe, suoni, specifici link a
                            pagine web di altri domini).
                        </p>
                        <p>
                            In funzione della loro durata, si distinguono in cookie di sessione (cioé quelli temporanei e cancellati automaticamente dal terminale al termine della sessione di
                            navigazione, chiudendo il browser) ed in cookie persistenti (cioé quelli che restano memorizzati sul terminale fino alla loro scadenza o cancellazione da parte dell'utente).
                        </p>
                        <p>
                            I cookie hanno diverse finalità. Sono in primo luogo utilizzati per la trasmissione della comunicazione o per fornire il servizio richiesto dall'utente; più precisamente
                            permettono di abilitare ed ottimizzare il funzionamento del sito internet, eseguire autenticazioni informatiche e prevenire abusi, monitorare le sessioni, migliorare l'esperienza
                            di navigazione degli utenti, ad esempio mantenendo attiva la connessione ad aree riservate durante la navigazione attraverso le pagine del sito senza la necessita di
                            reinserire User-Id e password e memorizzando informazioni specifiche riguardanti gli utenti stessi (tra cui le preferenze, il tipo di browser e di computer usato).
                        </p>
                        <p>
                            I suddetti cookie sono detti "tecnici" (per il loro utilizzo non é necessario il consenso dell'utente), in quanto senza di essi alcune delle citate operazioni non potrebbero
                            essere compiute o sarebbero più complesse e/o meno sicure.
                        </p>
                        <p>
                            Diversamente, se i cookie sono utilizzati per ulteriori finalità, tipicamente per analisi del comportamento ed invio di messaggi promo-pubblicitari personalizzati
                            (c.d. cookie di "profilazione") o anche solo per ottenere, per il tramite di servizi resi da terze parti, informazioni in forma aggregata sul numero degli utenti e su come
                            questi visitano il sito (c.d. "analytics cookie"), é necessario il consenso dell'utente. Infatti, prima dell'invio di questi cookie sul terminale, ai sensi della normativa
                            vigente (Codice privacy e provvedimento generale del Garante dell'8 maggio 2014), al momento in cui si accede alla home page o altra pagina del sito, é immediatamente mostrato
                            in primo piano un banner con una prima informativa sintetica sull'uso dei cookie e sulla raccolta del consenso, che l'utente può prestare proseguendo la navigazione tramite la
                            selezione di un elemento sottostante al banner o chiudendo il banner stesso.
                        </p>
                        <p>
                            In ogni caso, i cookie possono essere letti o modificati solo dal sito internet che li ha generati;  non possono essere utilizzati per richiamare nessun dato dal terminale
                            dell'utente e non possono trasmettere virus informatici. Alcune delle funzioni dei cookie possono essere svolte anche da altre tecnologie; pertanto, nel contesto della
                            presente web privacy policy, con il termine "cookie" si vuol far riferimento ai cookie ed a tutte le tecnologie similari.
                        </p>

